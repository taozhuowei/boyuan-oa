ALTER TABLE project ADD COLUMN IF NOT EXISTS foreman_employee_id BIGINT;

-- ============================================
-- 5. PAYROLL_REVENUE_CHANGE 表单类型 + 双向审批流（财务发起 → PM 审批；反之亦然）
--    审批节点的实际审批人由提交方反向决定，approver_type=ROLE 配合 approver_ref 在 service 中动态切换
-- ============================================
INSERT INTO form_type_def (code, name, is_enabled, is_system)
VALUES ('PAYROLL_REVENUE_CHANGE', '营收金额变更', TRUE, TRUE)
ON CONFLICT (code) DO NOTHING;

INSERT INTO approval_flow_def (business_type, version, is_active, created_at)
SELECT 'PAYROLL_REVENUE_CHANGE', 1, TRUE, CURRENT_TIMESTAMP
WHERE NOT EXISTS (SELECT 1 FROM approval_flow_def WHERE business_type = 'PAYROLL_REVENUE_CHANGE' AND is_active = TRUE);

-- 单节点：approver_ref 在 form_data 中通过 "counterparty" 字段动态决定
INSERT INTO approval_flow_node (flow_id, node_order, node_name, approval_mode, approver_type, approver_ref)
SELECT a.id, 1, '对方审批', 'SEQUENTIAL', 'ROLE', 'ceo'
FROM approval_flow_def a
WHERE a.business_type = 'PAYROLL_REVENUE_CHANGE' AND a.is_active = TRUE
AND NOT EXISTS (SELECT 1 FROM approval_flow_node n WHERE n.flow_id = a.id AND n.node_order = 1);

