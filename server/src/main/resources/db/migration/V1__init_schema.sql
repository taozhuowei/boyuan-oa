-- Flyway V1: Initial schema for Boyuan OA Platform

-- 用户表
CREATE TABLE IF NOT EXISTS sys_user (
    id BIGSERIAL PRIMARY KEY,
    username VARCHAR(50) NOT NULL UNIQUE,
    password VARCHAR(100) NOT NULL,
    email VARCHAR(100),
    phone VARCHAR(20),
    wx_user_id VARCHAR(50),
    status INT DEFAULT 1,
    create_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    update_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    deleted INT DEFAULT 0
);

-- 角色表
CREATE TABLE IF NOT EXISTS sys_role (
    id BIGSERIAL PRIMARY KEY,
    role_code VARCHAR(50) NOT NULL UNIQUE,
    role_name VARCHAR(50) NOT NULL,
    description VARCHAR(255),
    status INT DEFAULT 1,
    is_system INT DEFAULT 0,
    create_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    update_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    deleted INT DEFAULT 0
);

-- ============================================
-- 员工与组织
-- ============================================

-- 部门表
CREATE TABLE IF NOT EXISTS department (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    parent_id BIGINT,
    name VARCHAR(100) NOT NULL,
    sort INT DEFAULT 0,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    deleted INT DEFAULT 0
);

-- 岗位表
CREATE TABLE IF NOT EXISTS position (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    position_code VARCHAR(50) NOT NULL UNIQUE,
    position_name VARCHAR(100) NOT NULL,
    employee_category VARCHAR(20) NOT NULL CHECK(employee_category IN('OFFICE','LABOR')),
    default_role_code VARCHAR(50),
    supervisor_position_code VARCHAR(50),
    requires_construction_log BOOLEAN DEFAULT FALSE,
    has_performance_bonus BOOLEAN DEFAULT FALSE,
    base_salary DECIMAL(12,2),
    overtime_base_type VARCHAR(20) CHECK(overtime_base_type IN('BASE','TOTAL','CUSTOM')),
    overtime_base_amount DECIMAL(12,2),
    overtime_rate_weekday DECIMAL(4,2) DEFAULT 1.5,
    overtime_rate_weekend DECIMAL(4,2) DEFAULT 2.0,
    overtime_rate_holiday DECIMAL(4,2) DEFAULT 3.0,
    default_performance_bonus DECIMAL(12,2),
    annual_leave INT DEFAULT 0,
    leave_deduct_base_type VARCHAR(20) CHECK(leave_deduct_base_type IN('BASE','TOTAL','CUSTOM')),
    social_insurance_mode VARCHAR(20) CHECK(social_insurance_mode IN('COMPANY_PAID','MERGED')),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    deleted INT DEFAULT 0
);

-- 岗位级别表
CREATE TABLE IF NOT EXISTS position_level (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    position_id BIGINT NOT NULL REFERENCES position(id),
    level_name VARCHAR(100) NOT NULL,
    level_order INT DEFAULT 0,
    base_salary_override DECIMAL(12,2),
    performance_bonus_override DECIMAL(12,2),
    annual_leave_override INT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    deleted INT DEFAULT 0
);

-- 社保项目表
CREATE TABLE IF NOT EXISTS social_insurance_item (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    position_id BIGINT NOT NULL REFERENCES position(id),
    name VARCHAR(100) NOT NULL,
    employee_rate DECIMAL(5,4),
    company_rate DECIMAL(5,4),
    is_enabled BOOLEAN DEFAULT TRUE,
    display_order INT DEFAULT 0,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    deleted INT DEFAULT 0
);

-- 假期类型定义表
CREATE TABLE IF NOT EXISTS leave_type_def (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code VARCHAR(50) NOT NULL UNIQUE,
    name VARCHAR(50) NOT NULL,
    deduction_rate DECIMAL(4,2) DEFAULT 1.0,
    is_enabled BOOLEAN DEFAULT TRUE,
    is_system BOOLEAN DEFAULT FALSE,
    display_order INT DEFAULT 0,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    deleted INT DEFAULT 0
);

-- 员工表
CREATE TABLE IF NOT EXISTS employee (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_no VARCHAR(20) NOT NULL UNIQUE,
    password_hash VARCHAR(60),
    is_default_password BOOLEAN DEFAULT TRUE,
    name VARCHAR(50) NOT NULL,
    phone VARCHAR(20) UNIQUE,
    email VARCHAR(100),
    position_id BIGINT,
    level_id BIGINT,
    role_code VARCHAR(50),
    employee_type VARCHAR(50),
    direct_supervisor_id BIGINT,
    department_id BIGINT,
    account_status VARCHAR(20) DEFAULT 'ACTIVE' CHECK(account_status IN('ACTIVE','DISABLED')),
    entry_date DATE,
    leave_date DATE,
    base_salary_override DECIMAL(12,2),
    performance_base_override DECIMAL(12,2),
    salary_override_note VARCHAR(500),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    deleted INT DEFAULT 0
);

-- 项目表
CREATE TABLE IF NOT EXISTS project (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(200) NOT NULL,
    status VARCHAR(20) DEFAULT 'ACTIVE' CHECK(status IN('ACTIVE','CLOSED')),
    start_date DATE,
    actual_end_date DATE,
    log_cycle_days INT DEFAULT 1,
    log_report_cycle_days INT DEFAULT 1,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    deleted INT DEFAULT 0
);

-- 项目成员表
CREATE TABLE IF NOT EXISTS project_member (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    project_id BIGINT NOT NULL REFERENCES project(id),
    employee_id BIGINT NOT NULL REFERENCES employee(id),
    role VARCHAR(20) DEFAULT 'MEMBER' CHECK(role IN('PM','MEMBER')),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    deleted INT DEFAULT 0,
    UNIQUE(project_id, employee_id)
);

