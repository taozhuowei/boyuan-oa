);

-- 工资条表
CREATE TABLE IF NOT EXISTS payroll_slip (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cycle_id BIGINT NOT NULL REFERENCES payroll_cycle(id),
    employee_id BIGINT NOT NULL REFERENCES employee(id),
    version INT DEFAULT 1,
    status VARCHAR(20) DEFAULT 'DRAFT' CHECK(status IN('DRAFT','PUBLISHED','CONFIRMED','DISPUTED','SUPERSEDED')),
    net_pay DECIMAL(12,2),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    deleted INT DEFAULT 0
);

-- 工资条项目表（无 deleted 字段）
CREATE TABLE IF NOT EXISTS payroll_slip_item (
