-- 薪资周期表
CREATE TABLE IF NOT EXISTS payroll_cycle (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    period VARCHAR(20) NOT NULL,
    settlement_type VARCHAR(20) DEFAULT 'MONTHLY',
    start_date DATE,
    end_date DATE,
    pay_date DATE,
    window_days INT DEFAULT 7,
    window_status VARCHAR(20) CHECK(window_status IN('OPEN','CLOSED')),
    window_start_date DATE,
    window_end_date DATE,
    status VARCHAR(30) DEFAULT 'OPEN' CHECK(status IN('OPEN','WINDOW_OPEN','WINDOW_CLOSED','SETTLED','LOCKED','CORRECTING')),
    locked_at TIMESTAMP,
    version INT DEFAULT 1,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    deleted INT DEFAULT 0
);

-- 工资项定义表
CREATE TABLE IF NOT EXISTS payroll_item_def (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code VARCHAR(50) NOT NULL UNIQUE,
    name VARCHAR(100) NOT NULL,
    type VARCHAR(20) NOT NULL CHECK(type IN('EARNING','DEDUCTION')),
    display_order INT DEFAULT 0,
