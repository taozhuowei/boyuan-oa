-- 表单记录表
CREATE TABLE IF NOT EXISTS form_record (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    form_type VARCHAR(50) NOT NULL,
    submitter_id BIGINT REFERENCES employee(id),
    target_employee_id BIGINT REFERENCES employee(id),
    project_id BIGINT REFERENCES project(id),
    form_data TEXT,
    status VARCHAR(30) DEFAULT 'PENDING' CHECK(status IN('PENDING','APPROVING','APPROVED','REJECTED','ARCHIVED','RECALLED')),
    current_node_order INT DEFAULT 0,
    remark VARCHAR(500),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    deleted INT DEFAULT 0
);

-- 审批流定义表
CREATE TABLE IF NOT EXISTS approval_flow_def (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    business_type VARCHAR(50) NOT NULL,
    version INT DEFAULT 1,
    is_active BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    deleted INT DEFAULT 0
);

-- 审批流节点表
CREATE TABLE IF NOT EXISTS approval_flow_node (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    flow_id BIGINT NOT NULL REFERENCES approval_flow_def(id),
    node_order INT NOT NULL,
    node_name VARCHAR(100) NOT NULL,
    approval_mode VARCHAR(30) DEFAULT 'SEQUENTIAL',
    approver_type VARCHAR(30) NOT NULL CHECK(approver_type IN('DIRECT_SUPERVISOR','ROLE','DESIGNATED')),
    approver_ref VARCHAR(200),
    skip_condition TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    deleted INT DEFAULT 0
);

-- 审批记录表（无 deleted 字段）
CREATE TABLE IF NOT EXISTS approval_record (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    form_id BIGINT NOT NULL REFERENCES form_record(id),
    node_order INT NOT NULL,
    approver_id BIGINT REFERENCES employee(id),
    action VARCHAR(20) CHECK(action IN('APPROVE','REJECT','SKIP','RECALL')),
    comment VARCHAR(500),
    acted_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

